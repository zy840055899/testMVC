public class jinzhi {
    public static void main(String[] args) {
        int c = 0x12F;
        System.out.println("十进制:" + c);
        System.out.println("二进制" + Integer.toBinaryString(c));
        System.out.println("八进制" + Integer.toOctalString(c));
        System.out.println("十六进制" + Integer.toHexString(c));
    }
}
